            <p>La cifra distintiva della formazione in psicomotricità si evidenzia nell'attenzione alla FORMAZIONE
                PERSONALE del futuro professionista, alla costruzione di consapevolezza, alla conoscenza approfondita
                delle proprie caratteristiche relazionali, alla capacità di gestione del proprio patrimonio emozionale.
                Senza questa base, sono convinto, ogni abilità tecnica, seppur raffinata, ogni conoscenza teorica, per
                quanto approfondita, rischiano di disperdersi o svalutarsi, travolte dalla domanda esistenziale del
                giovane disabile, del bambino in difficoltà, dell'anziano istituzionalizzato.</p>

            <p>Questo è un problema storico delle istituzioni formative, che dovrebbe, teoricamente, essere superato. Ma
                non è così: di fronte alla fatica di porsi domande, anche come organizzazioni, si preferisce "rimuovere"
                e "compensare" con offerte di saperi e tecniche più o meno specialistici(a volte) e aggiornati (quasi
                mai). Con grande sperpero di risorse e di talenti.</p>

            <p>La formazione personale ha una metodologia elettiva: il gioco tra adulti,in gruppo.</p>

            <p>Nel gioco, l’azione è narrazione. Saper giocare è già cura di sé.</p>

            <p>Lo Psicomotricista, come ogni professionista implicato nella relazione di aiuto, in qualsiasi contesto si
                trovi ad operare è coinvolto in dinamiche relazionali complesse che possono indurre strategie difensive
                più o meno consapevoli.</p>

            <p>Proiezione, identificazione, idealizzazione, negazione, solo per esempio, non appartengono unicamente al
                “materiale” introdotto nella comunicazione dall’interlocutore (chiameremo così, in una dinamica
                comunicativa a due vie, chi viene definito ora paziente, utente, cliente…) ma sono anche produzioni del
                conduttore stesso, che deve saper riconoscere, accettare (e/o modificare) e governare.</p>

            <p>Si pensi solo all’impatto dell’operatore con situazioni di disabilità psicofisiche gravi, patologie
                psichiatriche, geriatriche o più quotidianamente, con bambini con gravi disturbi della
                comunicazione.</p>

            <p>L’esperienza sul campo evidenzia come una buona preparazione teorico-tecnica non sia sufficiente ad
                attivare quella comunicazione empatica, quella particolare presenza in ascolto attivo, che sono la base
                di un intervento educativo efficace.</p>

            <p>In Psicomotricità, in particolare, la comunicazione corporea amplifica la portata di questi fenomeni ed è
                fondamentale che l’operatore abbia una formazione approfondita su di sé, sulle proprie strategie
                comunicative e relazionali.</p>

            <p>Occorre dunque che la FORMAZIONE PERSONALE consenta di confrontarsi con la PRATICA dell’EMPATIA CORPOREA,
                del DIALOGO TONICO, dell’ATTIVITÀ LUDICO-SIMBOLICA tra adulti.</p>

            <p>Corpo-Gioco-Relazione, che sono i cardini dell’intervento psicomotorio, comportano il possesso di
                COMPETENZE personali approfondite, da acquisire con un percorso formativo sufficientemente lungo,
                intenso ed adeguatamente guidato.</p>

